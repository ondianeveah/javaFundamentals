package reflections.methodReflection;

public class MethodReflection {

    class Dog {
        public void display() {
            System.out.println("I am a dog.");
        }

        protected void eat() {
            System.out.println("I eat dog food.");
        }

        private void makeSound() {
            System.out.println("Bark Bark");
        }

    }
}
