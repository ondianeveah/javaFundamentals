package ifElse;

public class IfElse {
    public static void main(String[] args) {
        double number = 10.0;

        if(number < 0){
            System.out.println("The Number is positive.");
        }else{
            System.out.println("The number is not positive");
        }
        System.out.println("The statement is always executed.");
    }
}
